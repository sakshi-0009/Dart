void main(){
  int n = 4;
  if(n == 0){
    print("Zero");
  }else if(n == 1){
    print("One");
  }else if(n == 2){
    print("Two");
  }else if(n == 3){
    print("Three");
  }else if(n == 4){
    print("Four");
  }else if(n == 5){
    print("Five");
  }else{
    print("Entered number is greater than 5.");
  }
}